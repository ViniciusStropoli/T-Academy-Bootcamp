package Questao03;

public class Recompensa {
    private int xp;
    private String descricao;

    public Recompensa(int xp, String descricao) {
        this.xp = xp;
        this.descricao = descricao;
    }

    public int getXp() {
        return xp;
    }

    public String getDescricao() {
        return descricao;
    }
}
